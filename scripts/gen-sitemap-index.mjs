import fs from 'fs'
import path from 'path'

const SITE_URL = 'https://platinumzenith.com'
const root = process.cwd()
const publicDir = path.join(root, 'public')

const CHILD_SITEMAPS = [
  'sitemap.xml',
  'sitemap-images.xml',
  'sitemap-news.xml',
]

const existingChildren = CHILD_SITEMAPS.filter((name) => fs.existsSync(path.join(publicDir, name)))

if (existingChildren.length === 0) {
  throw new Error('No child sitemaps found. Expected sitemap.xml and/or sitemap-images.xml in public/.')
}

const xmlItems = existingChildren
  .map((name) => {
    const abs = path.join(publicDir, name)
    const lastmod = fs.statSync(abs).mtime.toISOString().slice(0, 10)

    return [
      '  <sitemap>',
      `    <loc>${SITE_URL}/${name}</loc>`,
      `    <lastmod>${lastmod}</lastmod>`,
      '  </sitemap>',
    ].join('\n')
  })
  .join('\n')

const xml = [
  '<?xml version="1.0" encoding="UTF-8"?>',
  '<sitemapindex xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
  '  <!-- Auto-generated by scripts/gen-sitemap-index.mjs -->',
  xmlItems,
  '</sitemapindex>',
  '',
].join('\n')

const outPath = path.join(publicDir, 'sitemap-index.xml')
fs.writeFileSync(outPath, xml, 'utf8')

console.log(`Generated sitemap index: ${outPath}`)
console.log(`Child sitemaps: ${existingChildren.length}`)
