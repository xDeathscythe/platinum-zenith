/**
 * Split blogData.js (1.5MB monolith) into:
 *   - blogDataIndex.js  — lightweight lookup: slug → { title, excerpt, date, category, readTime, schema, isDraft }
 *   - blogContent/<slug>.js — per-post content module: export default "..."
 *
 * BlogPostPage will then:
 *   1. import blogDataIndex to find post meta
 *   2. dynamic import(`./blogContent/${slug}.js`) to get content on demand
 *
 * This eliminates loading 1.5MB for a single blog post visit.
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const BLOG_DIR = join(ROOT, 'src', 'pages', 'blog')
const CONTENT_DIR = join(BLOG_DIR, 'blogContent')

// Read blogData.js as text
const src = readFileSync(join(BLOG_DIR, 'blogData.js'), 'utf8')

// We need to eval this to get the array. Use Function constructor.
// First, extract the rawBlogPosts array before the export logic.
const arrayEndMarker = '\nconst seenSlugs'
const arrayPart = src.substring(0, src.indexOf(arrayEndMarker))
// Wrap in a function that returns the array
const fn = new Function(arrayPart + '\nreturn rawBlogPosts;')
const posts = fn()

console.log(`Parsed ${posts.length} posts`)

// Create content directory
if (!existsSync(CONTENT_DIR)) mkdirSync(CONTENT_DIR, { recursive: true })

// Generate per-post content files
let generated = 0
for (const post of posts) {
  if (!post?.slug) continue
  const content = post.content || ''
  // Escape backticks and dollar signs for template literal
  const escaped = content.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$')
  const moduleContent = `// Auto-generated blog content for: ${post.slug}\nexport default \`${escaped}\`\n`
  writeFileSync(join(CONTENT_DIR, `${post.slug}.js`), moduleContent, 'utf8')
  generated++
}

console.log(`Generated ${generated} content modules in blogContent/`)

// Generate blogDataIndex.js — everything EXCEPT content
const indexEntries = posts.filter(p => p?.slug).map(post => {
  const { content, ...meta } = post
  return meta
})

const indexSrc = `// Auto-generated by scripts/split-blog-data.mjs
// Blog metadata index — NO content (lazy-loaded per post)
// Regenerate with: node scripts/split-blog-data.mjs

const rawBlogIndex = ${JSON.stringify(indexEntries, null, 2)}

const seenSlugs = new Set()

export const blogPostIndex = rawBlogIndex.filter((post) => {
  if (!post?.slug) return false
  if (seenSlugs.has(post.slug)) return false
  seenSlugs.add(post.slug)
  return true
})

export const blogPostBySlug = new Map(blogPostIndex.map(p => [p.slug, p]))
`

writeFileSync(join(BLOG_DIR, 'blogDataIndex.js'), indexSrc, 'utf8')
console.log(`Generated blogDataIndex.js (${(Buffer.byteLength(indexSrc) / 1024).toFixed(1)} KB)`)

console.log('Done! Now update BlogPostPage.jsx to use blogDataIndex + dynamic content import.')
