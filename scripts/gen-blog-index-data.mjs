import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const blogDataPath = path.resolve(__dirname, '../src/pages/blog/blogData.js')
const blogIndexDataPath = path.resolve(__dirname, '../src/pages/blog/blogIndexData.js')

const { blogPosts = [] } = await import(`${pathToFileURL(blogDataPath).href}?t=${Date.now()}`)

const blogIndexPosts = blogPosts.map((post) => ({
  slug: post.slug,
  title: post.title,
  excerpt: post.excerpt,
  date: post.date,
  category: post.category,
  readTime: post.readTime,
  isDraft: Boolean(post.isDraft),
}))

const output = `// Auto-generated by scripts/gen-blog-index-data.mjs\n// Do not edit manually. Regenerate with: npm run seo:blog-index-data\n\nexport const blogIndexPosts = ${JSON.stringify(blogIndexPosts, null, 2)}\n`

await fs.writeFile(blogIndexDataPath, output, 'utf8')

console.log(`Generated ${blogIndexDataPath} with ${blogIndexPosts.length} posts`)
