import fs from 'fs'
import path from 'path'
import { blogIndexPosts } from '../src/pages/blog/blogIndexData.js'

const SITE_URL = 'https://platinumzenith.com'
const root = process.cwd()
const publicDir = path.join(root, 'public')

const NOW = new Date()
const NEWS_WINDOW_MS = 2 * 24 * 60 * 60 * 1000 // 2 days (Google News freshness window)

function escapeXml(value = '') {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;')
}

function parsePostDate(dateStr) {
  if (!dateStr) return null
  const date = new Date(`${dateStr}T00:00:00+01:00`)
  if (Number.isNaN(date.getTime())) return null
  return date
}

function toIsoDateTime(date) {
  return date.toISOString()
}

const recentNewsPosts = [...new Map(
  blogIndexPosts
    .filter((post) => post?.slug && !post?.isDraft)
    .map((post) => [post.slug, post]),
).values()]
  .filter((post) => {
    const d = parsePostDate(post.date)
    if (!d) return false
    return NOW.getTime() - d.getTime() <= NEWS_WINDOW_MS
  })
  .sort((a, b) => {
    const da = parsePostDate(a.date)?.getTime() || 0
    const db = parsePostDate(b.date)?.getTime() || 0
    return db - da
  })
  .slice(0, 1000)

const items = recentNewsPosts.map((post) => {
  const pubDate = parsePostDate(post.date) || NOW
  return [
    '  <url>',
    `    <loc>${SITE_URL}/blog/${escapeXml(post.slug)}</loc>`,
    '    <news:news>',
    '      <news:publication>',
    '        <news:name>Platinum Zenith</news:name>',
    '        <news:language>sr</news:language>',
    '      </news:publication>',
    `      <news:publication_date>${toIsoDateTime(pubDate)}</news:publication_date>`,
    `      <news:title>${escapeXml(post.title || 'Platinum Zenith Blog')}</news:title>`,
    '    </news:news>',
    '  </url>',
  ].join('\n')
})

const xml = [
  '<?xml version="1.0" encoding="UTF-8"?>',
  '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9" xmlns:news="http://www.google.com/schemas/sitemap-news/0.9">',
  '  <!-- Auto-generated by scripts/gen-news-sitemap.mjs -->',
  ...(items.length ? items : []),
  '</urlset>',
  '',
].join('\n')

const outPath = path.join(publicDir, 'sitemap-news.xml')
fs.writeFileSync(outPath, xml, 'utf8')

console.log(`Generated news sitemap: ${outPath}`)
console.log(`News URL entries: ${recentNewsPosts.length}`)
