import fs from 'fs'
import path from 'path'
import { pageMeta } from '../src/hooks/pageMetaData.js'
import { ogMeta } from '../server/ogMeta.js'
import { blogPosts } from '../src/pages/blog/blogData.js'

const SITE_URL = 'https://platinumzenith.com'
const today = new Date().toISOString().slice(0, 10)

const INTERNAL_EXCLUDE = new Set([
  '/dashboard',
  '/prijave',
  '/poruke',
  '/newsletter',
  '/emails',
  '/analytics',
  '/log',
])

function xmlEscape(value = '') {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;')
}

function normalizeRoute(route) {
  if (!route) return '/'
  if (route === '/') return '/'
  return route.endsWith('/') ? route.slice(0, -1) : route
}

function toAbsoluteUrl(url) {
  if (!url) return `${SITE_URL}/og-image.jpg`
  if (url.startsWith('http://') || url.startsWith('https://')) return url
  if (url.startsWith('/')) return `${SITE_URL}${url}`
  return `${SITE_URL}/${url}`
}

const entries = []

const staticRoutes = [...new Set(
  Object.keys(pageMeta)
    .map(normalizeRoute)
    .filter((route) => !INTERNAL_EXCLUDE.has(route) && !route.startsWith('/draft/')),
)]

for (const route of staticRoutes.sort((a, b) => a.localeCompare(b))) {
  const meta = pageMeta[route]
  const og = ogMeta[route] || {}
  const imageUrl = toAbsoluteUrl(og.ogImage || meta?.ogImage || '/og-image.jpg')
  const title = meta?.title || 'Platinum Zenith'

  entries.push({
    loc: `${SITE_URL}${route === '/' ? '' : route}`,
    imageUrl,
    title,
    lastmod: today,
  })
}

const publicBlogPosts = [...new Map(
  blogPosts
    .filter((post) => post?.slug && !post?.isDraft)
    .map((post) => [post.slug, post]),
).values()]

for (const post of publicBlogPosts.sort((a, b) => a.slug.localeCompare(b.slug))) {
  const imageUrl = toAbsoluteUrl(post.ogImage || '/og-image.jpg')
  const lastmod = /^\d{4}-\d{2}-\d{2}$/.test(post.date) ? post.date : today

  entries.push({
    loc: `${SITE_URL}/blog/${post.slug}`,
    imageUrl,
    title: post.title || 'Platinum Zenith Blog',
    lastmod,
  })
}

const uniqueEntries = [...new Map(entries.map((entry) => [entry.loc, entry])).values()]

const xmlItems = uniqueEntries
  .map((entry) => {
    return [
      '  <url>',
      `    <loc>${xmlEscape(entry.loc)}</loc>`,
      `    <lastmod>${entry.lastmod}</lastmod>`,
      '    <image:image>',
      `      <image:loc>${xmlEscape(entry.imageUrl)}</image:loc>`,
      `      <image:title>${xmlEscape(entry.title)}</image:title>`,
      '    </image:image>',
      '  </url>',
    ].join('\n')
  })
  .join('\n')

const xml = [
  '<?xml version="1.0" encoding="UTF-8"?>',
  '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9" xmlns:image="http://www.google.com/schemas/sitemap-image/1.1">',
  '  <!-- Auto-generated by scripts/gen-image-sitemap.mjs -->',
  ...(xmlItems ? [xmlItems] : []),
  '</urlset>',
  '',
].join('\n')

const outPath = path.join(process.cwd(), 'public', 'sitemap-images.xml')
fs.writeFileSync(outPath, xml, 'utf8')

console.log(`Generated image sitemap: ${outPath}`)
console.log(`URL entries: ${uniqueEntries.length}`)
